sub omer {
  my $self = shift;
  my $hdate = shift;
  if( $hdate->get_hmonth() == 7 ) {       # nisan
    if( $hdate->get_hday() > 15 ) {
      return( $hdate->get_hday() - 15 );
    }
  } elsif( $hdate->get_hmonth() == 8 ) {
    return( $hdate->get_hday() + 15 );
  } elsif( $hdate->get_hmonth() == 9 ) {
    if( $hdate->get_hday() < 6 ) {
      return( $hdate->get_hday() + 44 );
    }
  }
  return 0;
}

