# Plugin object constructor
 sub new {
     my $package = shift;
     my $confdir = shift;

     $confdir = 'data/HebrewCalendar' unless defined $confdir;
     $confdir = 'data/HebrewCalendar' if $confdir eq '';

     return bless {
         datadir => $confdir,
         triggers => {
              "\\sЖ[ЫИ]ДЫ?" => \&sayHebrewDate,
              "ЕВРЕ[ИЙ]" => \&sayHebrewDate,
         }, # $mask => \&coderef
         commands => {
              "(ЛЕХАИМ)|(МА НИШМА)" => \&sayHebrewDate_addressed,
              "((ГЛАВА)|(ТОР[АЫУ]))" => \&sayParsha,
         }, # $mask => \&coderef
     }, $package;
 }

 sub PCI_register {
     my ($self, $irc) = @_[0 .. 1];
     $self->SUPER::PCI_register( @_[1 .. $#_] );

     $irc->plugin_register( $self, 'SERVER', qw( bot_mentioned public_translated ) );

     return 1;
 }

 sub PCI_unregister {
     my ($self, $irc) = @_[0 .. 1];
     $self->SUPER::PCI_unregister( @_[1 .. $#_] );

     return 1;
 }



 sub sayHebrewDate_addressed {
   my ( $self, $who, $where, $line, $i_called, $request_text, $trigger, $triggered, $before_trigger, $after_trigger ) = @_;

   return $self->sayHebrewDate( $who, $where, $line, $trigger, $triggered, $before_trigger, $after_trigger );
 }

 sub sayHebrewDate {
   my ( $self, $who, $where, $line, $trigger, $triggered, $before_trigger, $after_trigger ) = @_;

   my $nick = parse_user( $who );

   my $r = $self->hebrewDate();
   my $l = $nick . ", сегодня ".$r->[0]." года. Глава этой недели - ".$r->[1].". ";
   $l .= sprintf("Суббота начнётся в %02d:%02d. ", int($r->[7]/60), ($r->[7]%60) ) if $r->[6] == 5;
   if( $r->[5] == 0 ) {
     $l .= "Сегодня ".$r->[2].".";
   } else {
     if( $r->[5] == 1 ) {
       $l .= "Завтра праздник - ".$r->[2].".";
     } elsif( $r->[5] == 2 ) {
       $l .= "Послезавтра праздник - ".$r->[2].".";
     } elsif( $r->[5] == 3 || $r->[5] == 4 ) {
       $l .= "Через ".$r->[5]." дня праздник - ".$r->[2].".";
     } elsif( $r->[5] <= 20 ) {
       $l .= "Скоро (через ".$r->[5]." дней, ".$r->[4].") праздник - ".$r->[2].".";
     } else {
       $l .= "Ближайший праздник - ".$r->[2].", он будет ".$r->[3]." года (".$r->[4].").";
     }
   }
   if( $r->[9] > 0 ) {
     $l .= " Сегодня ".$r->[9]."-й день счёта омера.";
   }
   $self->say( $where, $l );

   return PCI_EAT_ALL;
 }


sub sayParsha {
   my ( $self, $who, $where, $line, $i_called, $request_text, $trigger, $triggered, $before_trigger, $after_trigger ) = @_;

   my $nick = parse_user( $who );

   my $info = $self->aboutParsha( $self->hebrewDate()->[8] );
   if( $info->[1] eq "" ) {
     $self->action( $where, "не знает ничего про текущую главу ".$info->[0]." :-(" );
   } else {
    my $parstr = shift @$info;
    my $book = shift @$info;
    my $chapters = shift @$info;
    $self->say( $where, $nick .", Глава этой недели - $parstr. Она включает стихи $chapters книги $book. Краткое содержание:" );
    $self->say( $where, map( "  $_", @$info ) );
  }
}


